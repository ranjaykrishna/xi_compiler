package rak248.asm;

import java.util.ArrayList;

public class ExpressionInstruction extends Instruction{
	private AddressingMode addr;

	public ExpressionInstruction(){
		setTiles(new ArrayList<Tile>());
		addr = new AddressingMode(0);
	}
	public void setAddr(AddressingMode addr) {
		this.addr = addr;
	}

	public AddressingMode getAddr() {
		return addr;
	}
	

}
